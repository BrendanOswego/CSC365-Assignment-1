public class NodeEntry {

    Double tmax;
    Double tavg;
    String name;

    public Double getTmax() {
        return tmax;
    }

    public void setTmax(Double tmax) {
        this.tmax = tmax;
    }

    public Double getTavg() {
        return tavg;
    }

    public void setTavg(Double tavg) {
        this.tavg = tavg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
